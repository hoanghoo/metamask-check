async function getTransferToken({address}){
    const options = { chain: "mumbai", order: "desc", from_block: "0", address:address };
    const allTransactions = await Moralis.Web3API.account.getTokenTransfers(options);
    const txs = allTransactions.result.filter((value) => {
        if (tokenList.includes(value.address.toLowerCase())) return value
    })
    return txs;
}


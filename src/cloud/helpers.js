const tokenList = [BIVE_Address.toLowerCase(), VRA_Address.toLowerCase(), BIZ_Address.toLowerCase(), LP_Address.toLowerCase()];

async function getBalanceToken({address}){
    const options = { chain: "mumbai", order: "desc", from_block: "0", address:address };
    const balanceAll = await Moralis.Web3API.account.getTokenBalances(options);
    const balance = balanceAll.filter((value) => {
        if (tokenList.includes(value.token_address.toLowerCase())) return value
    })
    return balance
}

